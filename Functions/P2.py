''' 2. Declare a square() function with one parameter.Then call the function and pass one
number and display the square of that number . '''

# Make a function called square
def square(number):
    # Multiply the number by itself to get the square
    return number * number

# Use the square function with 4
result = square(4)

# Show the answer
print("The square of 4 is:", result)
