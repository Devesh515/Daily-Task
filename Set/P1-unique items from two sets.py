#1. Write a Python program to Get Only unique items from two sets.


# Defining two sets
set1 = {10, 20, 30, 40, 50}
set2 = {30, 40, 50, 60, 70}

# Getting the union of both sets to include all unique items
unique_items = set1 | set2  # Union of both sets

# Printing the result
print(unique_items)  

